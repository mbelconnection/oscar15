BEGIN;

CREATE SEQUENCE GLOBAL_ID_GENERATOR;

CREATE TABLE PROVIDER (
    PROVIDER_NO VARCHAR(10) NOT NULL,
    CONSTRAINT  PROVIDER_PK PRIMARY KEY (PROVIDER_NO),
    
	LAST_NAME VARCHAR(30) NOT NULL,
	FIRST_NAME VARCHAR(30) NOT NULL,
	PROVIDER_TYPE VARCHAR(15) NOT NULL,
	SPECIALTY VARCHAR(20) NOT NULL,
	TEAM VARCHAR(20),
	SEX CHAR(1) NOT NULL,
	DOB DATE,
	ADDRESS VARCHAR(40),
	PHONE VARCHAR(20),
	WORK_PHONE VARCHAR(50),
	OHIP_NO VARCHAR(20),
	RMA_NO VARCHAR(20),
	BILLING_NO VARCHAR(20),
	HSO_NO VARCHAR(10),
	STATUS CHAR(1),
	COMMENTS CLOB,
	PROVIDER_ACTIVITY CHAR(3)
);

CREATE TABLE SECURITY (
	SECURITY_NO NUMBER(10) NOT NULL,
	CONSTRAINT  SECURITY_PK PRIMARY KEY (SECURITY_NO),
	
	USER_NAME VARCHAR(30) NOT NULL,
	PASSWORD VARCHAR(80) NOT NULL,
	PROVIDER_NO VARCHAR(10),
	PIN VARCHAR(6),
	B_EXPIRESET NUMBER(1) DEFAULT 1,
	DATE_EXPIREDATE DATE DEFAULT '01-JAN-2100',
	B_LOCALLOCKSET NUMBER(1) DEFAULT 1,
	B_REMOTELOCKSET NUMBER(1) DEFAULT 1
);

ALTER TABLE SECURITY ADD
CONSTRAINT SECURITY_UK1
UNIQUE ("USER_NAME");

COMMIT;