package org.oscarehr.ws.rest.to.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class ConsultationServiceTo implements Serializable {
	private static final long serialVersionUID = 8731416468085375658L;
	private String id;
	private String description;
	private List<SpecialtyTo> specialties = new ArrayList<SpecialtyTo>();

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public List<SpecialtyTo> getSpecialties() {
		return specialties;
	}

	public void setSpecialties(List<SpecialtyTo> specialties) {
		this.specialties = specialties;
	}

}
